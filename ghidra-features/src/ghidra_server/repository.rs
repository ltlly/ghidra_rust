/// A repository manages a versioned filesystem and a user access list.
///
/// Matches Java's `ghidra.server.Repository`.
pub struct Repository {
    name: String,
    root_file: PathBuf,
    user_access_file: PathBuf,
    valid: Mutex<bool>,
    user_map: Mutex<HashMap<String, User>>,
    anonymous_access_allowed: Mutex<bool>,
    event_queue: Mutex<Vec<RepositoryChangeEvent>>,
    dispatch_suspended: Mutex<bool>,
}

impl Repository {
    /// Create or load a repository at the given path.
    ///
    /// # Arguments
    ///
    /// * `name` -- the repository name.
    /// * `root_file` -- the root directory of this repository.
    /// * `creator` -- the user creating the repository (or `None` if loading).
    pub fn new(
        name: impl Into<String>,
        root_file: PathBuf,
        creator: Option<&str>,
    ) -> Result<Self, ServerError> {
        let name = name.into();
        let acl_file = root_file.join(ACCESS_CONTROL_FILENAME);

        let repo = Self {
            name,
            root_file: root_file.clone(),
            user_access_file: acl_file,
            valid: Mutex::new(true),
            user_map: Mutex::new(HashMap::new()),
            anonymous_access_allowed: Mutex::new(false),
            event_queue: Mutex::new(Vec::new()),
            dispatch_suspended: Mutex::new(false),
        };

        if let Some(user) = creator {
            let mut map = repo.user_map.lock().map_err(|e| ServerError::Other(e.to_string()))?;
            map.insert(user.to_string(), User::new(user, Permission::Admin));
            drop(map);
            repo.write_user_list()?;
        } else {
            repo.read_access_file()?;
        }

        Ok(repo)
    }

    /// The repository name.
    pub fn name(&self) -> &str {
        &self.name
    }

    /// The root directory.
    pub fn root_file(&self) -> &Path {
        &self.root_file
    }

    /// Whether the repository is still valid (not deleted).
    pub fn is_valid(&self) -> bool {
        self.valid.lock().map(|v| *v).unwrap_or(false)
    }

    /// Check that the repository is valid.
    pub fn validate(&self) -> Result<(), ServerError> {
        if !self.is_valid() {
            Err(ServerError::Other("Repository has been deleted".into()))
        } else {
            Ok(())
        }
    }

    /// Whether anonymous access is allowed.
    pub fn anonymous_access_allowed(&self) -> bool {
        self.anonymous_access_allowed
            .lock()
            .map(|v| *v)
            .unwrap_or(false)
    }

    /// Get a user's access data.
    ///
    /// Returns `None` if the user is not found and anonymous access is not
    /// allowed.
    pub fn get_user(&self, username: &str) -> Option<User> {
        let map = self.user_map.lock().ok()?;
        if self.anonymous_access_allowed() && username == UserManager::ANONYMOUS_USERNAME {
            return Some(User::new(UserManager::ANONYMOUS_USERNAME, Permission::ReadOnly));
        }
        if let Some(user) = map.get(username) {
            return Some(user.clone());
        }
        if self.anonymous_access_allowed() {
            return Some(User::new(username, Permission::ReadOnly));
        }
        None
    }

    /// Verify that the user has admin privileges.
    pub fn validate_admin_privilege(&self, username: &str) -> Result<User, ServerError> {
        let user = self.get_user(username).ok_or_else(|| {
            ServerError::UserAccess(format!(
                "User {username} was not found in the '{}' repository access list",
                self.name
            ))
        })?;
        if !user.is_admin() {
            return Err(ServerError::UserAccess(format!(
                "User {username} does not have Admin privilege"
            )));
        }
        Ok(user)
    }

    /// Verify that the user has write privileges.
    pub fn validate_write_privilege(&self, username: &str) -> Result<User, ServerError> {
        let user = self.get_user(username).ok_or_else(|| {
            ServerError::UserAccess(format!(
                "User {username} was not found in the '{}' repository access list",
                self.name
            ))
        })?;
        if user.is_read_only() {
            return Err(ServerError::UserAccess(format!(
                "User {username} does not have write privilege"
            )));
        }
        Ok(user)
    }

    /// Verify that the user has read privileges.
    pub fn validate_read_privilege(&self, username: &str) -> Result<User, ServerError> {
        let user = self.get_user(username).ok_or_else(|| {
            ServerError::UserAccess(format!(
                "User {username} was not found in the '{}' repository access list",
                self.name
            ))
        })?;
        Ok(user)
    }

    /// Set the user access list for this repository.
    ///
    /// The current user must be an admin.
    pub fn set_user_list(
        &self,
        current_user: &str,
        users: &[User],
        allow_anonymous: bool,
    ) -> Result<(), ServerError> {
        self.validate_admin_privilege(current_user)?;

        let mut new_map = HashMap::new();
        let mut seen = HashSet::new();
        for user in users {
            if user.name() == UserManager::ANONYMOUS_USERNAME {
                continue;
            }
            if !user.has_write_permission() && !user.is_read_only() && !user.is_admin() {
                return Err(ServerError::Other(format!(
                    "Invalid permission for user: {}",
                    user.name()
                )));
            }
            if !seen.insert(user.name().to_string()) {
                return Err(ServerError::Other(format!(
                    "Duplicate user entry: {}",
                    user.name()
                )));
            }
            new_map.insert(user.name().to_string(), user.clone());
        }

        if !new_map.contains_key(current_user) {
            return Err(ServerError::UserAccess(
                "User may not remove or change permissions for self".into(),
            ));
        }
        if !new_map
            .get(current_user)
            .map(|u| u.is_admin())
            .unwrap_or(false)
        {
            return Err(ServerError::UserAccess(
                "User may not remove or change permissions for self".into(),
            ));
        }

        {
            let mut aa = self
                .anonymous_access_allowed
                .lock()
                .map_err(|e| ServerError::Other(e.to_string()))?;
            *aa = allow_anonymous;
        }
        {
            let mut map = self.user_map.lock().map_err(|e| ServerError::Other(e.to_string()))?;
            *map = new_map;
        }
        self.write_user_list()
    }

    /// Get the list of users for this repository.
    pub fn get_user_list(&self, current_user: &str) -> Result<Vec<User>, ServerError> {
        self.validate()?;
        if current_user == UserManager::ANONYMOUS_USERNAME {
            return Ok(Vec::new());
        }
        self.validate_read_privilege(current_user)?;
        let map = self.user_map.lock().map_err(|e| ServerError::Other(e.to_string()))?;
        Ok(map.values().cloned().collect())
    }

    /// Set a single user's permission.
    pub fn set_user_permission(
        &self,
        username: &str,
        permission: Permission,
    ) -> Result<bool, ServerError> {
        let mut map = self.user_map.lock().map_err(|e| ServerError::Other(e.to_string()))?;
        map.insert(username.to_string(), User::new(username, permission));
        self.write_user_list_inner(&map)?;
        Ok(true)
    }

    /// Remove a user from this repository.
    pub fn remove_user(&self, username: &str) -> Result<bool, ServerError> {
        let mut map = self.user_map.lock().map_err(|e| ServerError::Other(e.to_string()))?;
        if map.remove(username).is_some() {
            self.write_user_list_inner(&map)?;
            Ok(true)
        } else {
            Ok(false)
        }
    }

    /// Dispose the repository (mark as invalid).
    pub fn dispose(&self) {
        if let Ok(mut v) = self.valid.lock() {
            *v = false;
        }
    }

    // --- Event queue ---

    /// Queue a change event.
    pub fn send_change_event(&self, event: RepositoryChangeEvent) {
        let suspended = self
            .dispatch_suspended
            .lock()
            .map(|v| *v)
            .unwrap_or(false);
        if let Ok(mut queue) = self.event_queue.lock() {
            queue.push(event);
        }
        if !suspended {
            let _ = self.flush_change_events();
        }
    }

    /// Suspend immediate event dispatching.
    pub fn suspend_event_dispatching(&self) {
        if let Ok(mut s) = self.dispatch_suspended.lock() {
            *s = true;
        }
    }

    /// Flush all queued change events.
    pub fn flush_change_events(&self) -> Vec<RepositoryChangeEvent> {
        let mut queue = self.event_queue.lock().unwrap();
        let events: Vec<RepositoryChangeEvent> = queue.drain(..).collect();
        if let Ok(mut s) = self.dispatch_suspended.lock() {
            *s = false;
        }
        events
    }

    // --- Formatting ---

    /// Generate formatted user access permissions from a repository directory.
    pub fn get_formatted_user_permissions(repo_dir: &Path, pad: &str) -> String {
        let acl_file = repo_dir.join(ACCESS_CONTROL_FILENAME);
        let mut buf = String::new();
        match Self::read_access_file_static(&acl_file) {
            Ok((users, anon)) => {
                if anon {
                    buf.push_str(&format!("{pad}* Anonymous read-only access permitted *\n"));
                }
                let mut sorted = users;
                sorted.sort();
                for user in sorted {
                    buf.push_str(&format!("{pad}{user}\n"));
                }
            }
            Err(e) => {
                buf.push_str(&format!("{pad}Failed to read repository access file: {e}\n"));
            }
        }
        buf
    }

    // --- Private helpers ---

    fn read_access_file(&self) -> Result<(), ServerError> {
        if !self.user_access_file.exists() {
            return Ok(());
        }
        let (users, anon) = Self::read_access_file_static(&self.user_access_file)?;
        {
            let mut aa = self
                .anonymous_access_allowed
                .lock()
                .map_err(|e| ServerError::Other(e.to_string()))?;
            *aa = anon;
        }
        let mut map = self.user_map.lock().map_err(|e| ServerError::Other(e.to_string()))?;
        *map = HashMap::new();
        for user in users {
            map.insert(user.name().to_string(), user);
        }
        Ok(())
    }

    fn read_access_file_static(path: &Path) -> Result<(Vec<User>, bool), ServerError> {
        let file = fs::File::open(path).map_err(|e| ServerError::Io(e.to_string()))?;
        let reader = BufReader::new(file);
        let mut users = Vec::new();
        let mut allow_anonymous = false;

        for line_result in reader.lines() {
            let line = line_result.map_err(|e| ServerError::Io(e.to_string()))?;
            let trimmed = line.trim();
            if trimmed.starts_with(COMMENT_PREFIX) || trimmed.is_empty() {
                continue;
            }
            if trimmed == ANONYMOUS_STR {
                allow_anonymous = true;
                continue;
            }
            if let Some(user) = Self::process_access_line(trimmed) {
                users.push(user);
            }
        }
        Ok((users, allow_anonymous))
    }

    fn process_access_line(line: &str) -> Option<User> {
        let pos = line.find('=')?;
        if pos == 0 {
            return None;
        }
        let username = line[..pos].trim().to_string();
        let type_name = line[pos + 1..].trim();
        let permission = Permission::from_name(type_name)?;
        Some(User::new(username, permission))
    }

    fn write_user_list(&self) -> Result<(), ServerError> {
        let map = self.user_map.lock().map_err(|e| ServerError::Other(e.to_string()))?;
        self.write_user_list_inner(&map)
    }

    fn write_user_list_inner(
        &self,
        user_map: &HashMap<String, User>,
    ) -> Result<(), ServerError> {
        let anon = self
            .anonymous_access_allowed
            .lock()
            .map(|v| *v)
            .unwrap_or(false);

        let mut content = String::new();
        content.push_str(";\n");
        content.push_str(&format!(
            "; User Access List for {}: Auto-generated\n",
            self.name
        ));
        content.push_str(";\n");

        if anon {
            content.push_str(ANONYMOUS_STR);
            content.push('\n');
        }

        for user in user_map.values() {
            content.push_str(&format!(
                "{}={}\n",
                user.name(),
                user.permission().as_str()
            ));
        }

        let tmp = self.user_access_file.with_extension("tmp");
        {
            let mut f = fs::File::create(&tmp).map_err(|e| ServerError::Io(e.to_string()))?;
            f.write_all(content.as_bytes()).map_err(|e| ServerError::Io(e.to_string()))?;
        }
        fs::rename(&tmp, &self.user_access_file).map_err(|e| ServerError::Io(e.to_string()))?;
        Ok(())
    }
}

impl Drop for Repository {
    fn drop(&mut self) {
        self.dispose();
    }
}

