//! Repository management for the Ghidra Server.
//!
//! Ported from `ghidra.server.Repository`.  Each [`Repository`] manages
//! a versioned local filesystem and a set of users with permissions.

use std::collections::{HashMap, HashSet};
use std::fmt;
use std::fs;
use std::io::{BufRead, BufReader, Write};
use std::path::{Path, PathBuf};
use std::sync::Mutex;

use super::user_manager::{Permission, User, UserManager};
use super::ServerError;

/// Anonymous read-only user constant.
const ANONYMOUS_STR: &str = "=ANONYMOUS_ALLOWED";

/// Comment prefix in the access control file.
const COMMENT_PREFIX: char = ';';

/// File name for the access control list.
const ACCESS_CONTROL_FILENAME: &str = "userAccess.acl";

/// Permission type name mapping.
const TYPE_NAMES: [&str; 3] = ["READ_ONLY", "WRITE", "ADMIN"];

// ---------------------------------------------------------------------------
// RepositoryChangeEvent
// ---------------------------------------------------------------------------

/// Types of repository change events (mirrors Java's `RepositoryChangeEvent`).
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum RepositoryChangeKind {
    /// A folder was created.
    FolderCreated,
    /// A folder was deleted.
    FolderDeleted,
    /// A folder was moved.
    FolderMoved,
    /// A folder was renamed.
    FolderRenamed,
    /// An item (file) was created.
    ItemCreated,
    /// An item (file) was deleted.
    ItemDeleted,
    /// An item (file) was moved.
    ItemMoved,
    /// An item (file) was renamed.
    ItemRenamed,
    /// An item (file) was changed.
    ItemChanged,
    /// The server is shutting down.
    ServerShutdown,
}

/// A change event generated by the repository filesystem.
#[derive(Debug, Clone)]
pub struct RepositoryChangeEvent {
    /// The kind of change.
    pub kind: RepositoryChangeKind,
    /// The parent path of the changed entity.
    pub parent_path: String,
    /// The name of the changed entity.
    pub name: String,
    /// The new parent path (for moves), or `None`.
    pub new_parent_path: Option<String>,
    /// The new name (for renames), or `None`.
    pub new_name: Option<String>,
}

impl RepositoryChangeEvent {
    /// Create a new change event.
    pub fn new(
        kind: RepositoryChangeKind,
        parent_path: impl Into<String>,
        name: impl Into<String>,
        new_parent_path: Option<String>,
        new_name: Option<String>,
    ) -> Self {
        Self {
            kind,
            parent_path: parent_path.into(),
            name: name.into(),
            new_parent_path,
            new_name,
        }
    }
}

impl fmt::Display for RepositoryChangeEvent {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(
            f,
            "{:?} {}/{}",
            self.kind, self.parent_path, self.name
        )
    }
}

