//! Data type merge support.
//!
//! Ports Ghidra's `DataTypeMerger`, `EnumMerger`, `StructureMerger`,
//! `UnionMerger`, and `DataTypeMergeException`.

mod enum_merger;
mod structure_merger;
mod union_merger;

pub use enum_merger::EnumMerger;
pub use structure_merger::StructureMerger;
pub use union_merger::UnionMerger;

use crate::base::merge::error::{DataTypeMergeError, MergeResult};

/// A component of a composite data type (struct field or union member).
///
/// Port of Ghidra's `DataTypeComponent`.
#[derive(Debug, Clone)]
pub struct MergeDataTypeComponent {
    /// The ordinal position within the composite.
    pub ordinal: usize,
    /// The byte offset of this component within the composite.
    pub offset: usize,
    /// The length of this component in bytes.
    pub length: usize,
    /// The name of this field (may be `None` for anonymous fields).
    pub field_name: Option<String>,
    /// The data type name of this component.
    pub data_type_name: String,
    /// Comment associated with this component.
    pub comment: Option<String>,
}

impl MergeDataTypeComponent {
    /// Create a new component descriptor.
    pub fn new(
        ordinal: usize,
        offset: usize,
        length: usize,
        field_name: Option<String>,
        data_type_name: impl Into<String>,
        comment: Option<String>,
    ) -> Self {
        Self {
            ordinal,
            offset,
            length,
            field_name,
            data_type_name: data_type_name.into(),
            comment,
        }
    }
}

/// An enum value entry: `(name, value, comment)`.
#[derive(Debug, Clone)]
pub struct EnumEntry {
    /// The name of this enum constant.
    pub name: String,
    /// The numeric value.
    pub value: i64,
    /// Optional comment.
    pub comment: Option<String>,
}

impl EnumEntry {
    /// Create a new enum entry.
    pub fn new(name: impl Into<String>, value: i64, comment: Option<String>) -> Self {
        Self {
            name: name.into(),
            value,
            comment,
        }
    }
}

/// A merged data type result produced by a merger.
#[derive(Debug, Clone)]
pub struct MergedDataType {
    /// The name of the merged type.
    pub name: String,
    /// The category path.
    pub category_path: String,
    /// The description of the merged type.
    pub description: Option<String>,
    /// Components of the merged composite (for struct/union).
    pub components: Vec<MergeDataTypeComponent>,
    /// Enum entries (for enum types).
    pub enum_entries: Vec<EnumEntry>,
    /// Size in bytes.
    pub size: usize,
    /// Whether packing is enabled (for structures).
    pub packing_enabled: bool,
    /// Warnings generated during merge.
    pub warnings: Vec<String>,
}

impl MergedDataType {
    /// Create a new merged data type with defaults.
    pub fn new(name: impl Into<String>, size: usize) -> Self {
        Self {
            name: name.into(),
            category_path: "/".to_string(),
            description: None,
            components: Vec::new(),
            enum_entries: Vec::new(),
            size,
            packing_enabled: false,
            warnings: Vec::new(),
        }
    }
}

