//! Data-flow and control-flow analysis transforms.
//!
//! This module contains the decompiler analysis pipeline:
//! 1. **CFG construction** ([`cfg`]) -- build control-flow graph from P-code
//! 2. **SSA form** ([`ssa`]) -- static single assignment construction
//! 3. **Data-flow analysis** ([`dataflow`]) -- reaching definitions, ranges, constants
//! 4. **Expression simplification** ([`simplify`]) -- constant folding, algebraic identities
//! 5. **C output generation** ([`output`]) -- formatted C source from structured IR
//!
//! Also included:
//! - **Control-flow structuring** ([`control_flow_struct`]) -- region-based loop/if/switch detection
//! - **C output formatter** ([`c_output`]) -- structured node to C text
//! - **Data-flow engine** ([`dataflow_engine`]) -- varnode-level def-use analysis
//! - **Type recovery** ([`type_recovery`]) -- constraint-based type inference

pub mod c_output;
pub mod cfg;
pub mod control_flow_struct;
pub mod dataflow;
pub mod dataflow_engine;
pub mod output;
