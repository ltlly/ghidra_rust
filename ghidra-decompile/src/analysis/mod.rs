/// The decompiler analysis pipeline.
///
/// Orchestrates the full decompilation flow from raw P-code sequences
/// (generated by SLEIGH) to structured, type-annotated C output.
///
/// # Pipeline stages
///
/// | Stage | Description |
/// |-------|-------------|
/// | 1. CFG | Build control-flow graph from P-code sequences |
/// | 2. SSA | Convert to static single assignment form |
/// | 3. Data-flow | Reaching definitions, constant propagation, range analysis |
/// | 4. Simplify | Constant folding, algebraic identities, dead code elimination |
/// | 5. Structuring | Control flow structuring (if/else, loops, switch) |
/// | 6. Output | Format as C source code |
#[derive(Debug)]
pub struct AnalysisPipeline {
    /// The input P-code sequences (one per machine instruction).
    pub sequences: Vec<PcodeSequence>,
    /// The constructed control-flow graph.
    pub cfg: Option<cfg::ControlFlowGraph>,
    /// The SSA form of the function.
    pub ssa: Option<ssa::SsaForm>,
    /// Per-varnode constant values discovered during propagation.
    pub constants: HashMap<Varnode, u64>,
    /// Simplified P-code operations.
    pub simplified_ops: Vec<PcodeOperation>,
    /// Pipeline configuration options.
    pub options: PipelineOptions,
}

/// Options controlling the analysis pipeline.
#[derive(Debug, Clone)]
pub struct PipelineOptions {
    /// Enable constant propagation.
    pub constant_propagation: bool,
    /// Enable copy propagation.
    pub copy_propagation: bool,
    /// Enable dead-code elimination.
    pub dead_code_elimination: bool,
    /// Enable expression simplification.
    pub expression_simplification: bool,
    /// Enable range analysis (value-set abstract interpretation).
    pub range_analysis: bool,
    /// Enable type recovery.
    pub type_recovery: bool,
}

impl Default for PipelineOptions {
    fn default() -> Self {
        Self {
            constant_propagation: true,
            copy_propagation: true,
            dead_code_elimination: true,
            expression_simplification: true,
            range_analysis: true,
            type_recovery: true,
        }
    }
}

impl AnalysisPipeline {
    /// Create a new analysis pipeline from P-code sequences.
    pub fn new(sequences: Vec<PcodeSequence>) -> Self {
        Self {
            sequences,
            cfg: None,
            ssa: None,
            constants: HashMap::new(),
            simplified_ops: Vec::new(),
            options: PipelineOptions::default(),
        }
    }

    /// Create a pipeline with custom options.
    pub fn with_options(sequences: Vec<PcodeSequence>, options: PipelineOptions) -> Self {
        Self {
            sequences,
            cfg: None,
            ssa: None,
            constants: HashMap::new(),
            simplified_ops: Vec::new(),
            options,
        }
    }

    // ------------------------------------------------------------------
    // Stage 1: Build CFG
    // ------------------------------------------------------------------

    /// Build the control-flow graph from P-code sequences.
    pub fn build_cfg(&mut self) -> GhidraResult<&cfg::ControlFlowGraph> {
        let cfg = cfg::build_cfg(&self.sequences)?;
        self.cfg = Some(cfg);
        Ok(self.cfg.as_ref().unwrap())
    }

    // ------------------------------------------------------------------
    // Stage 2: Build SSA
    // ------------------------------------------------------------------

    /// Build SSA form from the CFG and P-code operations.
    ///
    /// Requires stage 1 (CFG) to have been completed first.
    pub fn build_ssa(&mut self) -> GhidraResult<&ssa::SsaForm> {
        let cfg = self.cfg.as_ref()
            .ok_or_else(|| ghidra_core::error::GhidraError::InvalidState(
                "CFG must be built before SSA".into()
            ))?;

        // Flatten all operations across all sequences.
        let all_ops: Vec<PcodeOperation> = self.sequences
            .iter()
            .flat_map(|s| s.flatten())
            .collect();

        // Build the dominator tree.
        let dom = ssa::DominatorTree::compute(cfg);

        // Build SSA.
        let builder = ssa::SsaBuilder::new(all_ops, cfg.clone(), dom);
        let ssa_form = builder.build();
        self.ssa = Some(ssa_form);
        Ok(self.ssa.as_ref().unwrap())
    }

    // ------------------------------------------------------------------
    // Stage 3: Data-flow analysis
    // ------------------------------------------------------------------

    /// Run data-flow analysis passes: constant propagation, copy propagation,
    /// range analysis.
    ///
    /// Requires stage 1 (CFG) to have been completed first.
    pub fn run_dataflow(&mut self) -> GhidraResult<()> {
        let _cfg = self.cfg.as_ref()
            .ok_or_else(|| ghidra_core::error::GhidraError::InvalidState(
                "CFG must be built before data-flow analysis".into()
            ))?;

        let all_ops: Vec<PcodeOperation> = self.sequences
            .iter()
            .flat_map(|s| s.flatten())
            .collect();

        // Constant propagation.
        if self.options.constant_propagation {
            let mut cp = dataflow::ConstantPropagation::new();
            self.constants = cp.analyze(&all_ops)
                .into_iter()
                .filter_map(|(vn, cv)| {
                    cv.value().map(|v| (vn, v))
                })
                .collect();
        }

        // Copy propagation.
        if self.options.copy_propagation {
            let mut propagator = dataflow::CopyPropagation::new();
            propagator.propagate(&all_ops, &self.constants);
            // Merge discovered copies back into constants.
            for (dst, src_vn) in propagator.copies() {
                if let Some(&val) = self.constants.get(&src_vn) {
                    self.constants.insert(dst.clone(), val);
                }
            }
        }

        // Range analysis.
        if self.options.range_analysis {
            let mut ra = dataflow::RangeAnalysis::new();
            ra.analyze(&all_ops, &self.constants);
            // Augment constants with range-derived values.
            for (vn, vs) in ra.ranges() {
                if let Some(val) = vs.as_constant() {
                    self.constants.entry(vn.clone()).or_insert(val as u64);
                }
            }
        }

        Ok(())
    }

    // ------------------------------------------------------------------
    // Stage 4: Simplify
    // ------------------------------------------------------------------

    /// Run expression simplification: constant folding, algebraic identities,
    /// dead-code elimination.
    pub fn run_simplification(&mut self) -> GhidraResult<()> {
        let all_ops: Vec<PcodeOperation> = self.sequences
            .iter()
            .flat_map(|s| s.flatten())
            .collect();

        let mut ops = all_ops;

        // Expression simplification.
        if self.options.expression_simplification {
            let simplifier = simplify::ExpressionSimplifier::with_constants(
                self.constants.clone()
            );
            ops = simplifier.simplify(&ops);
        }

        // Dead-code elimination.
        if self.options.dead_code_elimination {
            ops = dataflow::DeadCodeElimination::eliminate(&ops);
        }

        self.simplified_ops = ops;
        Ok(())
    }

    // ------------------------------------------------------------------
    // Run all stages
    // ------------------------------------------------------------------

    /// Run the complete analysis pipeline in order.
    ///
    /// This is the recommended entry point for most callers. It executes
    /// stages 1-4 in sequence and returns the CFG for further processing
    /// (structuring and output generation).
    pub fn run_all(&mut self) -> GhidraResult<&cfg::ControlFlowGraph> {
        self.build_cfg()?;
        self.build_ssa()?;
        self.run_dataflow()?;
        self.run_simplification()?;
        Ok(self.cfg.as_ref().unwrap())
    }

    /// Returns the constructed CFG, if available.
    pub fn cfg(&self) -> Option<&cfg::ControlFlowGraph> {
        self.cfg.as_ref()
    }

    /// Returns the SSA form, if available.
    pub fn ssa(&self) -> Option<&ssa::SsaForm> {
        self.ssa.as_ref()
    }

    /// Returns the discovered constants.
    pub fn constants(&self) -> &HashMap<Varnode, u64> {
        &self.constants
    }

    /// Returns the simplified operations in execution order.
    pub fn simplified_ops(&self) -> &[PcodeOperation] {
        &self.simplified_ops
    }
}

