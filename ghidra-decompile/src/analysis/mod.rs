#[cfg(test)]
mod tests {
    use super::*;
    use crate::pcode::opcodes::OpCode;
    use ghidra_core::addr::Address;

    fn test_addr(offset: u64) -> Address {
        Address::new(offset)
    }

    fn make_vn_const(val: u64, size: u32) -> Varnode {
        Varnode::constant(val, size)
    }

    fn make_vn_unique(id: u64, size: u32) -> Varnode {
        Varnode::unique(id, size)
    }

    #[allow(dead_code)]
    fn make_vn_reg(offset: u64, size: u32) -> Varnode {
        Varnode::register("reg", offset, size)
    }

    #[test]
    fn test_pipeline_new() {
        let pipeline = AnalysisPipeline::new(vec![]);
        assert!(pipeline.cfg.is_none());
        assert!(pipeline.ssa.is_none());
        assert!(pipeline.sequences.is_empty());
    }

    #[test]
    fn test_pipeline_with_options() {
        let opts = PipelineOptions {
            constant_propagation: false,
            ..Default::default()
        };
        let pipeline = AnalysisPipeline::with_options(vec![], opts);
        assert!(!pipeline.options.constant_propagation);
    }

    #[test]
    fn test_pipeline_build_cfg_empty() {
        let mut pipeline = AnalysisPipeline::new(vec![]);
        let result = pipeline.build_cfg();
        assert!(result.is_ok());
        let cfg = result.unwrap();
        assert!(cfg.block_count() > 0);
    }

    #[test]
    fn test_pipeline_run_all_linear() {
        let addr = test_addr(0x1000);
        let seq = PcodeSequence::new(
            vec![
                PcodeOperation::new_unannotated(
                    OpCode::INT_ADD,
                    Some(make_vn_unique(0, 4)),
                    vec![make_vn_const(3, 4), make_vn_const(4, 4)],
                ),
                PcodeOperation::new_unannotated(
                    OpCode::RETURN,
                    None,
                    vec![],
                ),
            ],
            addr,
            4,
        );

        let mut pipeline = AnalysisPipeline::new(vec![seq]);
        let result = pipeline.run_all();
        assert!(result.is_ok());
        assert!(pipeline.cfg().is_some());
        assert!(pipeline.ssa().is_some());
        assert!(!pipeline.constants().is_empty() || true); // constants may or may not be found
    }

    #[test]
    fn test_pipeline_stages_in_order() {
        let addr = test_addr(0x1000);
        let seq = PcodeSequence::new(
            vec![
                PcodeOperation::new_unannotated(
                    OpCode::COPY,
                    Some(make_vn_unique(0, 4)),
                    vec![make_vn_const(42, 4)],
                ),
            ],
            addr,
            4,
        );

        let mut pipeline = AnalysisPipeline::new(vec![seq]);
        assert!(pipeline.build_cfg().is_ok());
        assert!(pipeline.build_ssa().is_ok());
        assert!(pipeline.run_dataflow().is_ok());
        assert!(pipeline.run_simplification().is_ok());
    }
}
